# int
x = 20
print(type(x))

# float
y = 2.40
print(type(y))


# bool
isAlive = True
print(type(isAlive))
isMarried = False
print(type(isMarried))


# string
name = 'ashik'
print(name)
print(type(name))


# list
nums = [1, 2, 3, 4]
print(nums)
print(type(nums))

heroes = ['ironman', 'thor', 'doctor strange']
print(heroes)
print(type(heroes))


# tuple
coordinates = (2, 4)
print(coordinates)
print(type(coordinates))

colors = ('red', 'green', 'yellow')
print(colors)
print(type(colors))

# dictionary

details = {
    "name": "mohan",
    "age": 20,
    "city": "delhi",
}
print(details)
print(type(details))
